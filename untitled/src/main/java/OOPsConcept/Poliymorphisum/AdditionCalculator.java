package OOPsConcept.Poliymorphisum;

public class AdditionCalculator {

    int result;

    AdditionCalculator(){
        this.result = result;
    }

    public int add(int a, int b){
        result = a + b;
        return result;
    }

    public double add(double a, double b){
        return  a + b;
    }
}
